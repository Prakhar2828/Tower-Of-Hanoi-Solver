package towerofhanoi;

// -------------------------------------------------------------------------
/**
 * An enumeration representing positions used in the context of the Tower of
 * Hanoi puzzle.
 * 
 * @author Prakhar Pandey
 * @version 16-Oct-2023
 */
public enum Position
{
    /**
     * Represents the leftmost position.
     */
    LEFT,

    /**
     * Represents the middle position.
     */
    MIDDLE,

    /**
     * Represents the rightmost position.
     */
    RIGHT,

    /**
     * Represents a default or unspecified position.
     */
    DEFAULT;
}
